# Dictionary
# Key and Value
# name -> "Vimal"

# A dictionary is an unordered collection of data
# in a key-value pair format.

my_dict2 = {
    "name": "Vimal",
    "age": 32,
    "address": "Rajkot"
}

print(len(my_dict2))
print(my_dict2["name"])
print(my_dict2["age"])
print(my_dict2["address"])
my_dict2["name"] = "Rahul"
print(my_dict2)


