# List  - Mutable

shopping_list = ["Milk", "Bread", "Butter", "Poha", "Curd"]
# print(shopping_list)
# print(len(shopping_list))
# print(shopping_list[0])
# print(shopping_list[-1])

print(shopping_list)
shopping_list.insert(3, "Jam")
print(shopping_list)
shopping_list.extend(["Chips", "Salt"])
print(shopping_list)
shopping_list.remove("Bread")  # Remove item
print(shopping_list)
shopping_list.pop()  # Last item remove
print(shopping_list)
shopping_list.reverse()
print(shopping_list)
shopping_list.sort()
print(shopping_list)
# shopping_list.clear()
shopping_list.copy()
print(shopping_list)

